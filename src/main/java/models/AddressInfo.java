package models;

public class AddressInfo {
    private String address ;
    private String subLocality ;
    private String city ;
    private String state ;
    private String pincode ;
    private String shopLandmark ;
    private String additional_details ;
    private String marketPlace ;
    private Double latitude ;
    private Double longitude ;
public String getAddress() {
	return address;
}
public void setAddress(String address) {
	this.address = address;
}
public String getSubLocality() {
	return subLocality;
}
public void setSubLocality(String subLocality) {
	this.subLocality = subLocality;
}
public String getCity() {
	return city;
}
public void setCity(String city) {
	this.city = city;
}
public String getState() {
	return state;
}
public void setState(String state) {
	this.state = state;
}
public String getPincode() {
	return pincode;
}
public void setPincode(String pincode) {
	this.pincode = pincode;
}
public String getMarketPlace() {
	return marketPlace;
}
public void setMarketPlace(String marketPlace) {
	this.marketPlace = marketPlace;
}
public Double getLatitude() {
	return latitude;
}
public void setLatitude(Double latitude) {
	this.latitude = latitude;
}
public Double getLongitude() {
	return longitude;
}
public void setLongitude(Double longitude) {
	this.longitude = longitude;
}
public String getShopLandmark() {
	return shopLandmark;
}
public void setShopLandmark(String shopLandmark) {
	this.shopLandmark = shopLandmark;
}
public String getAdditional_details() {
	return additional_details;
}
public void setAdditional_details(String additional_details) {
	this.additional_details = additional_details;
}

  
}
